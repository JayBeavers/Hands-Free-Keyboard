        internal static IEnumerable<string> ExtractHistoryWords(string history)
        {
            var wordStart = 0;
            do
            {
                wordStart += history.PunctuationLength(wordStart);

                var wordLength = history.WordLength(wordStart);

                var word = history.Substring(wordStart, wordLength).ToLowerInvariant();

                yield return word;

                wordStart += wordLength;
            }
            while (wordStart < history.Length);
        }
